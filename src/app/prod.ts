export class Prod {
    Id: string;
    name: string;
    price: string;
    description: string;
    created: string;
    added: string;

    constructor(Id:string,name:string,price:string,description:string,created:string,added:string){
      this.Id=Id;
      this.name=name;
      this.price=price;
      this.description=description;
      this.created=created;
      this.added=added;
    }
}
